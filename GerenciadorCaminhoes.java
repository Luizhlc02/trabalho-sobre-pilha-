package Pilha;

public class GerenciadorCaminhoes {
	public String chassi; 
	public String carga;
	public boolean carregado;
	public String placa;
	public boolean perecivel;
	
public GerenciadorCaminhoes (String chassi, String carga, boolean carregado, String placa, boolean perecivel) {
		this.chassi = chassi;
		this.carga = carga;
		this.carregado = carregado;
		this.placa = placa;
		this.perecivel = perecivel; 
		
				
}
}